#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		i += write(1, "(null)", 6);
		return (6);
	}
	while (*s != '\0')
	{
		i += write(1, s, 1);
		s++;
	}
	return (i);
}
